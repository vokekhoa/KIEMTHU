package User;

/**
 *
 * @author ACER
 */
public class UserInf {
    private String id;
    private String name;
    private String pass;
    private String role;
    
    /**
     * @return the id
     */
    
    public UserInf(String id, String name, String pass, String role) {
        this.id = id;
        this.name = name;
        this.pass = pass;
        this.role = role;
    }

    public UserInf() {
    }

    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * @return the name
     */
    
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the pass
     */
    public String getPass() {
        return pass;
    }

    /**
     * @param pass the pass to set
     */
    public void setPass(String pass) {
        this.pass = pass;
    }

}
